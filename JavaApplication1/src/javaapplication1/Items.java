    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
     
        if (txtID.getText().isEmpty() || txtItem.getText().isEmpty() || txtPrice.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all fields.");
        return;
        }

        String sql = """
        MERGE dbo.product AS target
        USING (SELECT ? AS ProductID) AS source
        ON target.ProductID = source.ProductID
        WHEN MATCHED THEN
            UPDATE SET
                ProductName = ?,
                Price = ?,
                Category = ?
        WHEN NOT MATCHED THEN
            INSERT (ProductID, ProductName, Price, Category)
            VALUES (?, ?, ?, ?);
        """;

        try (Connection con = Conn.getConnection();
             PreparedStatement pst = con.prepareStatement(sql)) {

            String id = txtID.getText();
            String name = txtItem.getText();
            int price = Integer.parseInt(txtPrice.getText());
            String category = txtCateg.getSelectedItem().toString();

            // MATCH
            pst.setString(1, id);

            // UPDATE
            pst.setString(2, name);
            pst.setInt(3, price);
            pst.setString(4, category);

            // INSERT
            pst.setString(5, id);
            pst.setString(6, name);
            pst.setInt(7, price);
            pst.setString(8, category);

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Product saved successfully.");
            tblview();
            btnClearActionPerformed(null);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "SQL Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

