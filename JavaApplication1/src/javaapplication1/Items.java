    private void displaySortedRowsFromDB() {
      try {
        // Check if the connection is null or closed, and reconnect if needed
        if (con == null || con.isClosed()) {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mojostore?useSSL=false&serverTimezone=UTC", "root", "");
        }

        DefaultTableModel model = new DefaultTableModel();
        tbl.setModel(model);

        String selectedCategory = txtItemID.getSelectedItem().toString(); // Getting the selected category from JComboBox

        String query;
        if (selectedCategory.equals("Select all")) {
            // Fetch all rows and order them by Category
            query = "SELECT * FROM product ORDER BY Category";
            pst = con.prepareStatement(query);
        } else {
            // Fetch rows based on selected category and order them by a specific column (you can modify the column name to your needs)
            query = "SELECT * FROM product WHERE Category = ? ORDER BY ProductName"; // Adjust column for sorting if needed
            pst = con.prepareStatement(query);
            pst.setString(1, selectedCategory);
        }

        rs = pst.executeQuery();

        // Get metadata to fetch column names
        ResultSetMetaData rsmd = rs.getMetaData();
        int columnsNumber = rsmd.getColumnCount();

        // Add columns to the table model
        for (int i = 1; i <= columnsNumber; i++) {
            model.addColumn(rsmd.getColumnName(i));
        }

        // Add rows to the table model
        while (rs.next()) {
            Object[] row = new Object[columnsNumber];
            for (int i = 1; i <= columnsNumber; i++) {
                row[i - 1] = rs.getObject(i);
            }
            model.addRow(row);
        }
    } catch (SQLException ex) {
        Logger.getLogger(Items.class.getName()).log(Level.SEVERE, null, ex);
    }}
    
