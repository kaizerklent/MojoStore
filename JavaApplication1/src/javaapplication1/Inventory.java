 private void IDgen(){
    try (Connection con = Conn.getConnection()) {

        String sql = "SELECT TOP 1 StockID FROM inventory ORDER BY StockID DESC";
        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            String last = rs.getString("StockID");  // Example: IN1005

            // Extract prefix ("IN") and number ("1005")
            String prefix = last.replaceAll("[0-9]", "");
            String number = last.replaceAll("[^0-9]", "");

            int num = Integer.parseInt(number) + 1;

            txtStockID.setText(prefix + num);  // IN1006
        } else {
            txtStockID.setText("IN1000");
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "IDgen Error: " + e.getMessage());
    }
 }
