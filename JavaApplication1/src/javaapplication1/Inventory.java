    private void itemID() {
           try (Connection con = Conn.getConnection()) {
        PreparedStatement pst = con.prepareStatement("SELECT DISTINCT ProductID FROM product");
        ResultSet rs = pst.executeQuery();

        txtItemID.removeAllItems();
        while (rs.next()) {
            txtItemID.addItem(rs.getString("ProductID"));
        }

    } catch (SQLException ex) {
        Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
    }
}
    
    public void inventoryview(){
        try (Connection con1 = Conn.getConnection()) {

            PreparedStatement pst = con1.prepareStatement("EXEC GetInventory");
            ResultSet res = pst.executeQuery();
            ResultSetMetaData Rss = res.getMetaData();
            int c = Rss.getColumnCount();

            DefaultTableModel Df = (DefaultTableModel) inventory.getModel();
            Df.setRowCount(0);

            while (res.next()) {
                Vector<Object> row = new Vector<>();
                row.add(res.getString("StockID"));
                row.add(res.getString("ProductID"));
                row.add(res.getString("Quantity"));
                row.add(res.getString("Batch"));
                Df.addRow(row);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Inventory.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
